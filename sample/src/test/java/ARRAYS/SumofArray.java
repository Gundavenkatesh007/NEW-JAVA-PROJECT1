package ARRAYS;

public class SumofArray {
	public static void main(String args[]) {
		int [] arr =  new int []{1,2,8,20,7};
		int sum = 0;
		for(int i=0;i < arr.length;i++) {
			sum =sum + arr[i];
		}
		System.out.println("Sum of all elements  of an array:" +sum);
					
		}
		
	}


