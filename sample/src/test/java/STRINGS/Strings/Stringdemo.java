package STRINGS.Strings;

public class Stringdemo {
	/* Two  ways to create a String.
	 1.By creating object. //output will be stored in Heap memory
	 2.BY directly giving output statement between (" "). // output will be stored in String constant pool.
	*/
	public static void main(String args[]) {
		String s1 = new String("Spiderman");
		String s2 = "No way Home";
		System.out.println(s1);
		System.out.println(s2);
	}
}

		
		
	


